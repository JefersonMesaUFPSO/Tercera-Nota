package main;

public class Calculator {
    public int sumar(int numA, int numB){
        return numA + numB;
    }

    public int restar(int numA, int numB){
        return numA - numB;
    }

    public int multiplicar(int numA, int numB){
        return numA * numB;
    }

    public int dividir(int numA, int numB){
        return numA / numB;
    }
}
